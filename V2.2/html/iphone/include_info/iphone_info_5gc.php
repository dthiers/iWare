	<h2>Prijs iPhone 5c</h2>

	<p>De iPhone 5c is een middenklassetoestel. Hoewel je voor een los
	toestel nog een behoorlijke prijs betaalt (vanaf €599) ben je met een
	abonnement veel goedkoper uit. De iPhone 5c concurreert met goedkopere
	toestellen van Samsung, Nokia en LG. Voor dat geld krijg je een
	gloednieuw toestel van de bekende Apple-kwaliteit, niet een model van
	een paar jaar geleden. Voor de echte budgetkopers blijft de 8GB iPhone
	4S verkrijgbaar. De prijs van de iPhone 5c in Europese landen: 16GB
	iPhone 5c: €599 32GB iPhone 5c: €699 Daarmee is de iPhone 5c iets
	goedkoper dan de iPhone 5 en de nieuwe iPhone 5s. Door twee toestellen
	uit te brengen kan Apple een grotere doelgroep aanspreken. Mensen die
	van luxe houden kiezen de duurdere iPhone 5s met metalen behuizing,
	mensen die een wat lagere prijs zoeken nemen de vrolijk gekleurde iPhone
	5c. </p>

	<h2>Releasedatum iPhone 5c</h2>

	<p>De iPhone 5c is sinds 20 september verkrijgbaar in de VS, Duitsland,
	Frankrijk en andere landen. Nederland en België zijn op 25 oktober 2013
	aan de beurt. In december 2013 moet de iPhone 5c in 100 landen
	verkrijgbaar zijn. </p>

	<h2>Uiterlijk iPhone 5c</h2>

	<p>iPhone 5c blauwHet meest opvalllende van de iPhone 5c is de plastic
	behuizing. Door gebruik te maken van goedkopere en bestaande componenten
	uit de huidige iPhones kan Apple de prijs verlagen. Apple kan op deze
	manier beter concurreren met Android-toestellen in het middensegment,
	die vaak gratis bij een abonnement worden weggegeven. De behuizing van
	de iPhone 5c is gemaakt van polycarbonaat. Of zoals Jonathan Ive het in
	een video op de website zegt: “beautifully, unapologetically plastic.”
	Ook zijn er details aan het ontwerp gewijzigd, zoals de rondingen van de
	behuizing. De volumeknoppen aan de linkerkant van de iPhone 5 hebben
	plaatsgemaakt voor dunne langwerpige knopjes. Er zijn vijf versies van
	de iPhone 5c: groen, wit, blauw, roze en geel. Een leuk detail is dat de
	standaard ingestelde achtergrond aansluit bij de kleur van je iPhone, al
	hebben alle modellen de beschikking over dezelfde achtergronden. Het
	scherm heeft net als de iPhone 5 een diameter van 4 inch en biedt 1136
	bij 640 beeldpunten. De iPhone 5c is zwaarder dan de iPhone 5 (132
	versus 112 gram) en heeft iets andere afmetingen. In onderstaande video
	legt Jonathan Ive uit op welke manier Apple te werk is gegaan bij de
	ontwikkeling van de iPhone 5c. Onder andere kleur, geometrie, gewicht en
	gebruiksvriendelijkheid hebben een rol gespeeld. </p>

	<h2>Kleurrijke hoesjes</h2>

	<p>Apple heeft voor de iPhone 5c speciale hoesjes gemaakt in de kleuren
	wit, roze, geel, blauw, groen en zwart. Opvallend zijn de ronde gaten in
	de achterkant van de case. Op de website van Apple kunt je zelf een
	combinatie samenstellen van hoesje en telefoon, zodat je alvast kunt
	zien hoe de twee kleuren bij elkaar passen. De hoesjes staan voor $29
	dollar op de Amerikaanse Apple-website. iPhone 5c hoesjes achterkant</p>

	<h2>Specificaties iPhone 5c</h2>

	<p>In het dagelijkse gebruik doet de iPhone 5c niet onder voor de iPhone
	5. De iPhone 5c bevat dezelfde A6 processor, een 8 megapixelcamera en
	verbeterde FaceTime-camera aan de voorkant. Wel nieuw is dat de iPhone
	5c veel meer LTE-banden ondersteunt, waardoor de iPhone 5c geschikt is
	voor 4G-netwerken in Nederland en België. iPhone 5c groen iPhone 5c
	draait op iOS 7 De iPhone 5c wordt standaard geleverd met iOS 7. De
	introductie van iOS 7 valt samen met de introductie van de iPhone 5c en
	iPhone 5s, maar iOS 7 is ook op oudere toestellen te installeren.
	Belangrijke nieuwe eigenschappen van het besturingssysteem zijn een
	compleet nieuw uiterlijk, het bedieningspaneel voor snelle instellingen,
	FaceTime Audio om elkaar te bellen en AirDrop om bestanden uit te
	wisselen. iOS 7 heeft meer dan 200 nieuwe functies.  </p>

	<h2>Doelgroep iPhone 5c</h2>

	<p>Wie gaat de iPhone 5c kopen? Met de iPhone 5c richt Apple zich op
	mensen die voor het eerst een smartphone kopen, maar ook op mensen die
	een middenklasse toestel zoeken. In landen als China en India laat Apple
	nog veel marktaandeel liggen vanwege de hoge prijs. ZTE, Huawei en
	Xiaomi groeien razendsnel in China en zijn een bedreiging voor
	luxemerken zoals Apple, Samsung en HTC. Apple kan met de iPhone 5c een
	groter publiek aanspreken: mensen die bereid zijn iets meer te betalen
	voor een ‘echte iPhone’. Apple heeft wel vaker goedkopere uitvoeringen
	van producten uitgebracht. Bij de iPods verscheen een supergoedkope iPod
	shuffle zonder scherm en bij de iPad werd de iPad mini als budgetmodel
	uitgebracht. De iPhone 5c zal het goed doen in bijvoorbeeld China,
	Brazilië en andere opkomende markten, waar nog veel potentiële klanten
	zitten die Apple met de huidige iPhone niet zo gemakkelijk bereikt
	vanwege de prijs. De lagere prijs van de iPhone 5c brengt de iPhone
	binnen bereik van iedereen. </p>

