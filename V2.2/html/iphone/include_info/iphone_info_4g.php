	<p>De iPhone 4 is een smartphone ontworpen en op de markt gebracht door
	Apple Inc. Op 7 juni 2010 werd de iPhone 4 aangekondigd. Het toestel
	heeft enkele nieuwe functies zoals videobellen met FaceTime, een 5
	megapixel-camera aan de achterzijde met flitser, een tweede camera aan
	de voorzijde en een ingebouwde gyroscoop. Bovendien is het scherm
	verbeterd, waarbij de resolutie is verhoogd (Retina-display) naar 960
	bij 640 pixels, een verviervoudiging van het aantal pixels vergeleken
	met de vorige generatie iPhones. Ook is de iPhone 4 dunner en sneller
	(dankzij de Apple A4-chip) dan de iPhone 3GS. Op 24 juni 2010 werd de
	iPhone 4 in de Verenigde Staten en een handvol andere landen gelanceerd.
	Deze generatie iPhone was vanaf 30 juli 2010 in Nederland en België
	beschikbaar. Tijdens de maand die daaraan vooraf ging, werden op de
	Belgische site van eBay driemaal zoveel iPhones verkocht als in andere
	maanden, omdat bezitters van oudere modellen hun toestel van de hand
	wilden doen om het te vervangen door de iPhone 4. De iPhone wordt in
	Nederland verkocht door T-Mobile, KPN en Vodafone. Na de introductie van
	de iPhone 4S zijn alleen het witte en zwarte 8GB-model nog te koop.</p>

	<h2>Functies</h2>

	<p>Deze versie van de iPhone, die op 7 juni 2010 tijdens de Worldwide
	Developers Conference werd geïntroduceerd, bevat compleet nieuwe
	hardware en een nieuw design dat voortaan vooral bestaat uit glas en
	roestvast staal. De iPhone 4 maakt gebruik de Apple A4-chip, een door
	Apple ontwikkelde en door Samsung geproduceerde processor. Deze chip
	heeft een kloksnelheid van 1 GHz, maar is geklokt op 800 Mhz om batterij
	te besparen. De A4 was voor het eerst te vinden in de eerste generatie
	van de iPad, die eerder dat jaar werd geïntroduceerd. Daarnaast bevat de
	iPhone 4 512 MB werkgeheugen, twee keer zoveel als de iPhone 3GS en de
	eerste generatie iPad. Het is daarnaast één van de eerste telefoons die
	gebruik maakt van een micro-simkaart, een kleinere variant van de
	standaard simkaart. Het nieuwe design van de iPhone 4 heeft een glazen
	voor- en achterkant met daar omheen een roestvrijstalen band die tevens
	als antenne functioneert. Aan beide kanten zit op het glas een
	vetafstotende laag tegen vingerafdrukken. Het nieuwe antenne-ontwerp
	heeft na de lancering van de telefoon in juni 2010 voor ophef gezorgd,
	omdat het GSM-signaal bij een bepaalde manier van vasthouden makkelijker
	zou kunnen wegvallen.[2][3][4] Als reactie hierop besloot Apple om
	gratis hoesjes uit te delen. Gebruikers mochten het apparaat ook
	kosteloos terugbrengen naar de winkel.[5] De iPhone 4 is daarnaast
	dunner dan zijn voorganger, maar wel een paar gram zwaarder. De iPhone 4
	kreeg ook een extra, tweede microfoon aan de bovenkant van het apparaat,
	waardoor achtergrondgeluid kan worden weggefilterd. Ook is een gyroscoop
	toegevoegd, zit er een krachtigere en zuinigere gps-chip in en is er
	ondersteuning voor het snellere wifi-802.11n. De camera is ten opzichte
	van de iPhone 3GS is ook verbeterd. De iPhone 4 heeft een 5
	megapixelcamera met led-flitser. Er kan ook worden gefilmd in HD-720p
	30fps-kwaliteit. Er zit ook een VGA-camera aan de voorkant van het
	apparaat wat videobellen op de iPhone mogelijk moet maken.</p>

	<p>Een van de belangrijkste verbeteringen van de iPhone 4 is het nieuwe
	scherm. De iPhone 4 is voorzien van een IPS-scherm met
	ledachtergrondverlichting. Dit moet zorgen voor een grote kijkhoek, goed
	contrast en hoge helderheid. Het aantal pixels is in vergelijking met de
	vorige generatie iPhones verviervoudigd naar een resolutie van 960 bij
	640 pixels. Dit zorgt voor een pixeldichtheid van ongeveer 326 pixels
	per inch. De iPhone 4 is verkrijgbaar in de kleuren wit en zwart. Na de
	komst van de iPhone 4S, is alleen nog de 8GB versie te koop, de iPhone 4
	wordt dan geleverd met iOS 5.</p>

