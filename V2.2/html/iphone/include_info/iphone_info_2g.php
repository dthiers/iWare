	<p>De iPhone is een smartphone van Apple. Met de iPhone is het onder
		meer mogelijk te bellen, sms'en, mailen, internetten, muziek te
		beluisteren, foto's en video's te maken en te bekijken, en games te
		downloaden en te spelen. Dit apparaat was met de HTC Touch een van de
		eerste smartphones met een on-screen-toetsenbord. Het heeft geen
		mechanische toetsen om telefoonnummers of sms-berichten in te tikken. De
		iPhone gebruikt iOS, een aangepaste versie van Mac OS X (het
		besturingssysteem voor Apple Mac-computers). Deze software creëert onder
		andere de virtuele toetsen op het aanraakscherm voor de bediening.</p>
		
		<p>Over de iPhone werd al gespeculeerd lang voordat hij uitgebracht
		werdIn 2002 waren er al geruchten dat een iPhone in de maak was. In
		september 2005 kondigde Apple de Motorola ROKR E1 aan, de eerste mobiele
		telefoon die met iTunes kon worden gesynchroniseerd. Omdat de
		Apple-ontwerpers het tot 2006 te druk hadden met de ontwikkeling van de
		iPod-lijn, werd samenwerking met Motorola gezocht.[bron?] Steve Jobs gaf
		na de lancering toe dat hij niet gelukkig was met de ROKR.[5] In
		september 2006 verwijderde Apple de ROKR-ondersteuning in iTunes. In
		plaats daarvan bood iTunes ondersteuning voor een nog onbekende mobiele
		telefoon, waarmee niet alleen audio-, maar ook video- en foto-bestanden
		tussen telefoon en desktopcomputer uitgewisseld konden worden. Dit
		leidde in verschillende media tot speculaties over een eigen mobiele
		telefoon van Apple.</p>

		<h2>Geschiedenis</h2>


		<p>Dag van de lancering in een Apple Store in Californië. De iPhone werd
		op 9 januari 2007 door Steve Jobs gepresenteerd tijdens de MacWorld
		Conference & Expo. In de Verenigde Staten startte de verkoop op 29 juni
		2007. Duizenden mensen stonden in de rij voor de Apple Stores om hun
		eerste iPhones te bemachtigen. Bij de lancering waren twee versies van
		de iPhone verkrijgbaar, met 4 respectievelijk 8 gigabyte opslagruimte.
		Het 4GB-model kostte $ 499 en het 8GB-model $ 599. Verder moest een
		tweejarig telefoonabonnement van AT&T worden afgesloten. Abonnementen
		begonnen bij $ 59,99 Na de introductie in de VS Op 5 september 2007,
		slechts twee maanden na de introductie, kondigde Apple aan het 8GB-model
		voortaan voor $ 399 te verkopen. Het 4GB-model werd per direct uit de
		winkels gehaald. Dit kon rekenen op groot protest bij early adopters.
		Daarop schreef CEO van Apple, Steve Jobs, een open brief waarin hij
		uitlegde dat de prijsverlaging noodzakelijk was vanwege het kerstseizoen
		dat in de VS voor de deur stond. Jobs bood vroege kopers ter compensatie
		een tegoedbon van $ 100 aan. Introductie aan de wereld. Op 9 november
		2007 werd de eerste generatie iPhone (2G) in Europa geïntroduceerd. In
		eerste instantie was het model alleen in Duitsland (via T-Mobile) en het
		Verenigd Koninkrijk (via O2) verkrijgbaar. De introductie van de iPhone
		in Frankrijk (via Orange) vond plaats op 28 november 2007. Andere landen
		waar de eerste generatie iPhones via de officiële kanalen werd
		aangeboden, waren Oostenrijk en Ierland. De eerste generatie iPhones
		werd niet in de Benelux verkocht. Op 5 februari 2008 werd een 16GB-model
		gelanceerd.</p>

		<h2>Functies</h2>

		<p>De iPhone heeft een multifunctioneel touchscreen. De eerste
		generaties hadden een resolutie van 480 x 320 pixels (163 ppi). Vanaf
		die iPhone 4 is de resolutie verdubbeld naar 960 x 640 pixels (326 ppi)
		en vanaf de iPhone 5 is de resolutie 1136 x 640 pixels (326 ppi). Apple
		noemt dit Retina Display omdat de pixels (viermaal zoveel als bij de
		iPhone 3GS) nu zo dicht bij elkaar zitten dat het menselijk oog geen
		individuele pixels meer kan waarnemen. Het scherm wordt niet bediend met
		een stylus, maar met één vinger; met een tweede vinger ontstaan er meer
		mogelijkheden om via dit scherm het apparaat te besturen. Eén vinger
		wordt gebruikt bij het bedienen van eenvoudige functies; twee vingers
		worden gebruikt om bijvoorbeeld in en uit te zoomen op foto's, video's
		en websites in de webbrowser Safari. De technologie van
		'tweevingerbesturing' werd enkele jaren geleden door het bedrijf
		geïntroduceerd bij de touchpads van notebooks. Bij de iPhone gaat deze
		techniek een stuk verder. Apple noemt deze technologie Multi Touch. De
		Safaribrowser van de iPhone ondersteunt Adobe Flash niet. De iPhone
		speelt alleen filmpjes af in het H.264- en MP4-formaat. Een aparte
		YouTube-speler speelt een selectie af van YouTube-filmpjes die in het
		H.264-formaat worden aangeboden in plaats van het gebruikelijke
		Flash-formaat. Daarnaast wordt op de website van YouTube geen gebruik
		gemaakt van Flash als men vanaf een mobiel apparaat de site bezoekt. De
		iPhone 2G en 3G hebben een digitale 2 megapixelcamera, die alleen foto's
		maakt. De camera van de iPhone 3GS heeft een 3 megapixelcamera die ook
		filmpjes maakt in VGA-resolutie (640x480). De iPhone 4 heeft een 5
		megapixelcamera en kan ook filmpjes maken in HD-kwaliteit (1280x720). De
		iPhone 4S en de iPhone 5 hebben een 8 megapixelcamera die kan filmen in
		Full HD-kwaliteit (1920x1080). De lens zit, zoals gebruikelijk bij veel
		mobiele telefoons, aan de achterkant. Vanaf de iPhone 4 zit er ook een
		lens aan de voorkant van het toestel voor onder andere videobellen
		middels FaceTime. De iPhone kan verbinding maken met andere apparaten
		via internet, via Bluetooth en wifi. Hij maakt verbinding met internet
		via UMTS. Verder heeft het apparaat een afstandsdetector waardoor het
		aanraakscherm automatisch wordt uitgeschakeld en het volume van de
		muziek uit de luidspreker wordt verlaagd zodra de telefoon gaat en de
		gebruiker het toestel bij het gezicht houdt. De iPhone heeft een
		elektronische gyroscoop op basis van versnellingsmeters. Hiermee wordt
		bepaald in welke stand het toestel zich bevindt: staand, liggend of
		schuin. De gebruikersinterface wordt hierop aangepast. Een voorbeeld is
		de bladerfunctie: in de staande modus bladert men door een lijst van
		nummers door met de vinger over het scherm te gaan. Het systeem bootst
		dan als het ware een soort vliegwiel na waarop de menu's zijn
		aangebracht; men kan dit vliegwiel een "zet geven" om snel door de
		menu's heen te bladeren, of juist "afremmen" om ze te kunnen bekijken.
		In de liggende stand schakelt de iPhone bijvoorbeeld automatisch om naar
		de "Cover Flow"-interface, waarmee je, net als bij iTunes, door
		albumhoezen kunt bladeren. In 2008 bracht Apple een
		softwareontwikkelingspakket (SDK) voor het schrijven van iPhone- en iPod
		Touch-toepassingen uit. Met dit pakket kunnen bedrijven speciale
		iPhone-applicaties maken. Een basispakket was te koop voor $ 99; de
		uitgebreide versie voor $ 299.</p>


