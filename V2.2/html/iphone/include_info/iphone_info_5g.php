	<p>iPhone 5 werd op 12 september 2012 aangekondigd door Apple. De iPhone
	5 kreeg een groter 4-inch scherm en een snellere Apple A6-processor ten
	opzichte van z’n voorgangers. De iPhone 5 is geschikt voor 4G (LTE) waar
	je van kunt profiteren zodra Nederlandse operators hun 4G-netwerken
	beschikbaar stellen. Ook nieuw: aan de onderkant van de zit een
	Lightning-connector.</p>

	De iPhone 5 is sinds 28 september 2012 verkrijgbaar in Nederland en
	België. Meer informatie over de iPhone 5 kopen vind je in een apart
	artikel.

	In Nederland kost de iPhone 5: 16GB iPhone 5: €676 32GB iPhone 5: €778
	64GB iPhone 5: €879 Dit zijn de officiële prijzen in de Apple Store. In
	België zijn de prijzen respectievelijk €699 (16GB), €799 (32GB) en €919
	(64GB). De belangrijkste eigenschappen van de iPhone 5 zijn te zien in
	onderstaande promovideo. In een apart artikel hebben we de verschillen
	tussen de iPhone 5, iPhone 4S en iPhone 4 aangegeven.

	<h2>iPhone 5 verkrijgbaar sinds september 2012</h2> 
	<p>Op 21 september
	2012 wordt de iPhone 5 verkocht in de Verenigde Staten, Canada, Verenigd
	Koninkrijk, Frankrijk, Duitsland, Australië, Japan, Hongkong en
	Singapore. Nederland en België volgden een week later op 28 september.
	Verkrijgbaarheid iPhone 5 De iPhone 5 is simlockvrij via Vodafone, KPN,
	Hi, Telfort, T-Mobile en veel andere Nederlandse providers te koop. In
	België kun je terecht bij Mobistar, Telenet en andere providers. Een
	andere mogelijkheid is om de iPhone 5 als los toestel bij de (online)
	Apple Store of een APR (Apple Premium Reseller) te kopen. Je kunt je
	bestaande abonnement laten doorlopen of een sim only-abonnement
	afsluiten. De prijzen bij specifieke providers vind je in onderstaande
	artikelen: Vodafone KPN KPN Hi Telfort T-Mobile Telenet (België)</p>

	<h2>iPhone 5 heeft 4-inch scherm</h2> 
	<p>4-inch Retina iPhone 5Het
	4-inch scherm in de iPhone 5 heeft een resolutie van 1136 bij 640
	pixels, met een pixeldichtheid van 326 ppi. Dit betekent dat de iPhone 5
	een Retina-scherm heeft. Het scherm is in breedte niet veranderd, maar
	is juist wat hoger. Dit zorgt er ook voor dat er op het iconenscherm van
	de iPhone een extra rij voor app-iconen is. De apps van Apple zijn ook
	geoptimaliseerd. Hierdoor zie je bijvoorbeeld in de mail-applicatie meer
	e-mails en is er in de Kalender-app ruimte om een volle werkweek te
	laten zien. Ontwikkelaars moeten apps die in de App Store staan
	optimaliseren voor het nieuwe 4-inch scherm. Doen ze dit niet, dan
	verschijnen er zwarte balken aan de onder- en bovenkant van het beeld.
	Sinds 1 mei 2013 zijn ontwikkelaars verplicht de iPhone 5-resolutie te
	ondersteunen. Apple maakt voor het nieuwe 4-inch scherm gebruik van een
	nieuwe technologie, die het mogelijk maakt om de touchsensoren van de
	iPhone te integreren in het scherm. Hierdoor is er een schermlaag minder
	nodig, waardoor het touchscreen dunner wordt.</p>

	<h2>Nieuw uiterlijk voor iPhone 5</h2>

	<p>De iPhone 5 is volgens Apple de dunste iPhone die tot nu toe op de
	markt is gebracht. Hij is 7,6 millimeter dik, waardoor hij 18 procent
	dunner is dan de iPhone 4S. Daarnaast is hij 20 procent lichter. De
	glazen achterkant van de iPhone 4S is vervangen met een tweekleurige
	aluminium achterkant en de iPhone ziet er een stuk langwerpiger uit door
	het nieuwe 4-inch display. De audio-aansluiting zit voortaan aan de
	onderkant van de iPhone, vlak naast de speakers en lightning
	dockconnector. De zijkant van de zwarte iPhone 5 is donkerder dan de
	antenneband van de iPhone 4S.</p>

	<h2>iPhone 5 heeft nieuwe ‘lightning’ dock connector</h2>

	<p>In de iPhone 5 zit voor het eerst de Lightning-dockconnector. Hierin
	kun je een nieuwe 8-pins connector aansluiten, die er op twee
	verschillende manieren in past. De Lightning-connector is 80 procent
	kleiner dan de 30-pins connector. De nieuwe adapter zou volgens Apple
	ook duurzamer zijn dan de vorige dockconnector. Door de introductie van
	een nieuwe iPhone-aansluiting werken veel van je oude accessoires niet
	meer. Een nieuwe adapter van Apple maakt het echter mogelijk om alsnog
	een 30-pins-aansluiting in je iPhone 5 te steken.</p>

	<h2>iPhone 5 heeft 8-megapixelcamera met panoramamodus</h2>

	<p>De iPhone 5 heeft een 8-megapixelcamera die in grote lijnen hetzelfde
	is als de camera in de iPhone 4S. Softwareverbeteringen zorgen er echter
	voor dat de camera onder slechte lichtomstandigheden duidelijkere foto’s
	schiet. De camera is ook in staat om beelden van 1080p te filmen. Omdat
	de iPhone 5 is voorzien van een microfoon vlak onder de camera, wordt
	geluid bij het filmen goed opgevangen. De lens is gemaakt van
	saffierkristal, wat ervoor zorgt dat hij beter bestand tegen krassen is.
	Volgens Apple maakt de iPhone 5 ook 40 procent sneller foto’s dan de
	iPhone 4S, dankzij de A6-processor.</p> <p>iOS 6 maakt het mogelijk om
	met de iPhone 5 Panorama-foto’s te maken vanuit de Camera-applicatie.
	Hierbij beweeg je de iPhone van links naar rechts, om een breder beeld
	vast te leggen. Tijdens de presentatie van de iPhone 5 liet Apple een
	panoramafoto van 28 megapixel zien. De iPhone 4S ondersteunt ook de
	panoramafunctie. </p> <p>Aan de voorkant van de iPhone 5 zit een
	Facetime HD-camera. Hiermee kun je videogesprekken voeren met beelden
	van een resolutie van 720p. De stabiliteit van de camera en de
	gezichtsherkenning zijn allebei verbeterd.</p>

	<h2>Snelle A6-processor in de iPhone 5</h2>

	<p>iPhone 5 maakt gebruik van een snellere Apple A6-processor. De CPU
	hiervan is volgens Apple twee keer zo snel als de Apple A5, die je in de
	iPhone 4S vindt. Ook heeft de A6 twee keer zoveel grafische rekenkracht
	(GPU). De A6 is in staat om sneller apps te openen, sneller bewerkingen
	op te slaan en om mooiere games te draaien.</p>

	<h2>4G LTE-ondersteuning in de iPhone 5</h2>

	<p>Een nieuwe draadloze ontvanger in de iPhone 5 is voorzien van
	HSPDA+-, DC-CDHSDPA- en LTE-antennes. De iPhone kan automatisch wisselen
	tussen de antennes die op dat moment het beste signaal hebben, om op
	deze manier optimaal bereik te bieden. Verschillende providers zijn
	wereldwijd inmiddels bezig met het uitrollen van 4G-LTE netwerken, die
	zullen werken met de snellere LTE-antenne van de iPhone 5. Ook in
	Nederland wordt er op het moment getest met verschillende 4G-netwerken.
	In 2012 vond er een grote frequentieveiling plaats in Nederland, waarbij
	de frequenties voor 4G ook werden geveild. T-Mobile gebruikt de
	800Mhz-frequentie van de iPhone 5 voor 4G in Nederland vanaf de zomer
	van 2013. KPN en Vodafone komen rond diezelfde tijd ook met een
	4G-netwerk, maar deze zal enkel op selectie drukke locaties de iPhone 5
	ondersteunen. Over het algemeen gebruiken de twee de niet ondersteunde
	800Mhz-frequentie.</p>

	<h2>Aankondiging iPhone 5 op 12 september</h2>

	<p>apple eventDe iPhone 5 werd aangekondigd tijdens een speciaal
	persevenement op 12 september 2012. Tijdens het evenement werden ook een
	aantal andere producten aangekondigd, waaronder de nieuwe iPod nano en
	iPod touch. Apple deed ook de EarPods uit de doeken. Verder werd
	bekendgemaakt dat iOS 6 vanaf 19 september beschikbaar zal zijn voor de
	iPhone 3GS, iPhone 4, iPhone 4S, iPad 2, derde generatie iPad en vierde
	generatie iPod touch. De presentatie van Apple op 12 september 2012 is
	online terug te kijken. Dit kan via de volgende links: Nu kijken: Apple
	iPhone 5 keynote (directe link voor iOS)</p>

	<h2>iOS 6 en de iPhone 5</h2>

	<p>De iPhone 5 maakt gebruik van iOS 6, de nieuwste versie van het
	iOS-besturingssysteem. Hierdoor is de iPhone 5 in staat om gebruik te
	maken van een aantal nieuwe functies, zoals Apple’s nieuwe kaartendienst
	en Passbook. Meer over iOS 6 lees je in ons uitgebreide iOS 6-dossier.
	Apple kondigde aan dat iOS 6 vanaf 19 september 2012 ook beschikbaar zal
	zijn voor de iPhone 3GS, iPhone 4, iPhone 4S, iPad 2 en derde generatie
	iPad.</p>

