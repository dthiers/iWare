	<p>De iPhone 3G is een smartphone ontworpen en op de markt gebracht door
	Apple Inc. Op 9 juni 2008 kondigde Apple op de WWDC-conferentie het
	toestel aan. Vanaf 11 juli 2008 was dit model verkrijgbaar in 70 landen,
	in Nederland via T-Mobile, in België via Mobistar en in Luxemburg via
	zowel VOX als LuxGSM. Het toestel werd ook door Apple Premium Resellers
	verkocht. Een jaar later was de iPhone 3G verkrijgbaar in meer dan 80
	landen. Na de introductie van de iPhone 3GS was van de iPhone 3G tot 4
	juni 2010 alleen het zwarte 8GB-model nog te koop.</p>

	<h2>Functies</h2>

	<p>Deze versie van de iPhone die op 9 juni 2008 tijdens de WWDC werd
	geïntroduceerd, had een verbeterde e-mailfunctionaliteit, beveiliging
	voor ouders, assisted gps, een betere processor, een niet-verzonken
	hoofdtelefoonaansluiting en een verlengde stand-bytijd. De 3G in de naam
	betekent niet dat dit de derde versie van de telefoon was (het was
	namelijk de tweede versie), maar dat deze telefoon gebruik maakte van
	het 3G-netwerk. Daardoor werd breedbandinternetten nu ook mogelijk op de
	iPhone. Deze iPhone werd met iOS 2.0 uitgerust. Ook werd MobileMe
	geïntroduceerd, deze online dienst zorgt dat e-mail, adresgegevens,
	agenda's en foto's draadloos gesynchroniseerd worden met de Mac of pc,
	iPhone en de MobileMe-server. Deze iPhone lag vanaf 11 juli 2008 in de
	winkels. Een karakteristieke eigenschap van de iPhone is de "App Store"
	van producent Apple. Hier kan men extra programma's downloaden voor het
	gebruik op de iPhone. Veel applicaties zijn gratis, maar voor de
	betaalde varianten ontvangt Apple als aanbieder 30% van de opbrengst en
	de producenten de overige 70%. Binnen de eerste maand werden 60 miljoen
	applicaties gedownload (een omzet van 30 miljoen dollar). De iPhone 3G
	is sinds de introductie van de iPhone 3GS alleen maar in het zwart
	verkrijgbaar in een 8GB-editie. Daarvoor was de iPhone 3G ook
	beschikbaar in het wit voor de 16GB-versie.</p>

