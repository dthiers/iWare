	<p>Apple heeft op 20 september 2013 de iPhone 5s officieel uitgebracht in een aantal landen (maar nog niet in Nederland en België). Deze nieuwe iPhone heeft een vingerafdrukscanner (Touch ID), een dubbele ledflitser en een 64-bits A7-processor. De iPhone 5s is verkrijgbaar in spacegrijs, goud en zilver.
	iPhone 5s vanaf 25 oktober verkrijgbaar Sinds 20 september ligt de
	iPhone 5s in de Verenigde Staten, Australië, Canada, China, Frankrijk,
	Duitsland, Japan, Singapore en het Verenigd Koninkrijk officieel in de
	winkels. Nederland en België waren op 25 oktober 2013 aan de beurt. </p>

	<h2>vingerafdruksensor en verbeterde camera. </h2>

	<p>Prijs iPhone 5s De iPhone 5s is in Europese landen verkrijgbaar vanaf
	699 euro: 16GB iPhone 5s: €699 32GB iPhone 5s: €799 64GB iPhone 5s: €899
	</p>


	<h2>iPhone 5s in drie kleuren: spacegrijs, goud en zilver</h2>

	<p>iPhone 5s stapelDe iPhone 5s is beschikbaar in drie kleuren:
	(space)grijs, goud en zilver. Alleen de zilverwitte kleur kenden we al
	van de iPhone 5, de twee andere kleuren zijn nieuw in het assortiment.
	Wie nog meer kleur wil, kan terecht bij de iPhone 5c. Heb je moeite om
	een kleur te kiezen, dan hebben we een apart artikel met tips welke
	kleur iPhone 5s bij jou past. iPhone 5s ondersteunt 4G in Nederland De
	iPhone 5s ondersteunt veel meer LTE-banden dan de iPhone 5. Dat is goed
	nieuws in Nederland: dit betekent namelijk dat 4G-netwerken met de
	800Mhz-frequentie in Nederland ook worden ondersteund. Met andere
	woorden: de iPhone 5s (en iPhone 5C) kunnen met het nieuwe 4G-netwerk in
	Nederland verbinden. </p>


	<h2>iPhone 5s bevat A7-processor</h2>

	<p>Net als bij voorgaande iPhones gebruikt Apple voor de iPhone 5s een
	nieuwe processor. De A7-chip is sneller en krachtiger dan zijn
	voorgangers, wat betekent dat apps sneller opstarten en games er mooier
	uit kunnen zien. Het gebruik van een 64-bits kernel zorgt er volgens
	Apple zelfs voor dat de snelheid en grafische kracht allebei zijn
	verdubbeld. Een 64-bits A7-chip betekent dat apps anders worden
	ontwikkeld, maar volgens Apple blijven oude (32-bits) iOS-apps zonder
	problemen werken met de nieuwe chip. </p>


	<h2>Ontwerp iPhone 5s gelijk aan iPhone 5</h2>

	<p>iPhone 5sApple heeft er met de iPhone 5s voor gekozen om het ontwerp
	van de iPhone 5 nog eens te gebruiken. Dat is niet verrassend: bij de
	iPhone 3GS en iPhone 4S greep Apple ook naar het ontwerp van de iPhone
	3G en iPhone 4. De S-varianten van de iPhones bevatten vaak vooral
	interne verbeteringen, zoals een snellere processor (in dit geval de A7)
	en andere handige toevoegingen (zoals de vingerafdrukscanner in de
	iPhone 5s). Het gebruik van hetzelfde ontwerp betekent overigens ook dat
	de iPhone 5s een 4-inch-scherm heeft, net als de iPhone 5. </p>

	<h2>Touch ID: vingerafdrukscanner iPhone 5s</h2>

	<p>Hoewel het ontwerp van de iPhone 5s grotendeels gelijk is aan de
	iPhone 5, zijn er wel een aantal externe toevoegingen die de nieuwe
	iPhone onderscheiden. Eén van deze nieuwe onderdelen is de
	vingerafdrukscanner die in de homeknop van de iPhone 5s zit verwerkt.
	Met deze vingerafdruklezer, ook wel Touch ID genoemd, kan de iPhone zijn
	bezitter herkennen. Dit maakt het mogelijk om je iPhone te ontgrendelen
	of nieuwe apps te downloaden met een druk van je vinger. Je hoeft daarom
	niet meer wachtwoorden in te vullen. Apple zegt dat je vingerafdruk
	nooit wordt opgeslagen op Apple’s servers of iCloud.  </p>


	<h2>iPhone 5s: dubbele True Tone-ledflitser, verbeterde camera</h2>

	<p>iPhone 5s flitserDe iPhone 5s heeft een verbeterde camera met een
	f/2.2-diafragma en een sensor die 15 procent groter is. De nieuwe True
	Tone-flitser toont zowel warm als koud licht, wat door je iPhone slim
	wordt gecombineerd om de meest natuurlijke lichtomstandigheid mogelijk
	te creëren. In onderstaande afbeelding zie je een foto met een gewone
	flitser (links) naast een foto gemaakt met de dubbele ledflitser van de
	iPhone 5s.

	Verder grijpt Apple naar veel software-verbeteringen om de camera
	krachtiger te maken. In videomodus kan de iPhone-camera beelden met 120
	frames per seconden opnemen, die in slow-motion vloeiend afgespeeld
	kunnen worden. Een nieuwe burst mode laat je 10 foto’s vlak na elkaar
	maken, waarna de beste automatisch wordt geselecteerd. Onscherpe foto’s
	worden door de iPhone scherper gemaakt door meerdere beelden met elkaar
	te combineren. In onderstaande video legt Apple alle
	camera-verbeteringen in de iPhone 5s uit.</p>


	<h2>M7-processor geeft iPhone 5s bewegingscomputer</h2>

	<p>De iPhone 5s bevat een speciale motion coprocessor, die wordt
	gebruikt om continu bewegingsdata te meten. Deze informatie is
	toegankelijk voor iOS-apps, die hierdoor bijvoorbeeld kunnen aantonen
	hoeveel stappen je hebt gezet of andere bewegingsinformatie kunnen
	verschaffen. De iPhone-apps Nike+ Move zal hier onder andere gebruik van
	maken. Deze combineert data van de M7-chip, A7-chip en gps-ontvanger om
	je activiteiten op een dag in detail te visualiseren. .</p>


	<h2>iPhone 5s draait op iOS 7</h2>

	<p>Op de iPhone 5s staat standaard iOS 7 geïnstalleerd, de nieuwste
	versie van het iOS-besturingssysteem. iOS 7 introduceert een compleet
	nieuwe interface en ook de nodige nieuwe functies voor de iPhone. Meer
	hierover lees je in onze serie iOS 7 uitgelicht. iPhone 5s onder
	toezicht Steve Jobs ontworpen Lange tijd werd gedacht dat de iPhone 5
	het laatste toestel was waar Steve Jobs intensief bij was betrokken.
	Maar Apple heeft aan de Amerikaanse officier van justitie George Gascon
	verteld dat de iPhone 5s en de opvolger daarvan (iPhone 6?) ook nog
	onder nauw toezicht van Steve Jobs zijn ontworpen. Bij de iPhone 5s is
	dat op zich niet heel gek: het ontwerp lijkt immers sterk op de iPhone
	5. Dat Jobs ook heeft meegedacht over de iPhone 6 is echter
	interessant.</p>

