	<p>De iPhone 3GS is een smartphone ontworpen en op de markt gebracht
	door Apple Inc. Op 8 juni 2009 werd op de WWDC-conferentie de iPhone 3GS
	aangekondigd, waarbij de S staat voor Speed (snelheid). Deze werd vanaf
	26 juni 2009 in de Benelux verkocht.[1] Dit model bevatte nieuwe
	functies, onder andere een ingebouwd digitaal kompas, stembediening
	(voicecontrol) en een nieuwe 3,2 megapixelcamera met de mogelijkheid te
	filmen in VGA-kwaliteit. De iPhone 3GS is ongeveer tweemaal zo snel als
	de iPhone 3G. De iPhone 3GS werd oorspronkelijk gepresenteerd als de
	iPhone 3G S, maar werd een week na de introductie officieel hernoemd
	naar iPhone 3GS, zonder spatie. De naam is aangepast omdat dit beter
	voor zoekmachines op het internet is. Na de introductie van de iPhone 5
	is het zwarte 8GB-model uit de handel gehaald door Apple.</p>

	<h2>Functies</h2>

	<p>Deze versie van de iPhone, die op 8 juni 2009 tijdens de WWDC 2009
	werd geïntroduceerd, bevatte enkele nieuwe functies. Deze iPhone was
	gemiddeld twee keer zo snel als zijn voorganger, had tweemaal zoveel
	geheugen en een nieuwe processor. De iPhone 3GS ondersteunt 7,2-Mbps
	HSDPA voor hogere netwerksnelheden. Deze iPhone maakt gebruik van de
	OpenGL ES 2.0-standaard voor hoogwaardige 3D-beelden, waardoor de
	weergave in spelletjes en andere veeleisende grafische programma's beter
	is dan bij de vorige generaties. Ook is er een ingebouwd digitaal kompas
	dat zowel het geografische noorden als het magnetische noorden kan
	aanduiden. Het kompas wordt gebruikt in combinatie met gps, in
	verscheidene applicaties waaronder de standaardapplicatie "Kaarten". De
	iPhone 3GS is uitgerust met speciale toegankelijkheidsfuncties voor
	gebruikers die visueel gehandicapt zijn of gehoorproblemen hebben.
	Enkele voorbeelden van dergelijke functies zijn: de schermlezer van
	VoiceOver, een zoomfunctie, een optie voor wit-op-zwart-weergave en
	monogeluid. Ook de stembediening is nieuw. Voortaan kan iemand via
	spraak een telefoongesprek starten, een liedje starten of titel en
	artiest van het afgespeelde nummer opvragen. Verder gebruikt deze iPhone
	3GS bluetooth 2.1, in tegenstelling tot zijn voorganger die Bluetooth
	2.0 gebruikte. De iPhone 3GS heeft een vetafstotende laag tegen
	vingerafdrukken en kan worden bediend met de afstandsbediening via de
	koptelefoon.</p>

	<p>Hoewel Apple in het begin heeft gezegd dat ze niet meedoen aan de
	megapixel-hype (omdat megapixels niet altijd iets zouden zeggen over de
	kwaliteit), heeft het bedrijf de telefoon uiteindelijk toch een nieuwe
	camera gegeven. De telefoon bevat nu een 3 megapixel-camera met
	autofocus, autowitbalans, tap to focus (je drukt ergens op het scherm,
	en de camera focust op dat gebied). Ook kan de nieuwe camera filmen met
	30 beelden per seconde (fps) met VGA-kwaliteit. Ter vergelijking: de
	meeste televisieprogramma's gebruiken 25 fps. De 3GS heeft een
	verbeterde batterijcapaciteit, waardoor deze 30 uur lang audio kan
	afspelen (6 uur langer dan voorheen), 10 uur lang video afspelen (+3
	uur), 9 uur lang wifi gebruiken (+3 uur) en heeft hij 12 uur
	gesprekstijd op het 2G-netwerk (+2 uur). Ook de iPhone 3GS heeft een
	update gekregen naar de zesde release van de firmware van Apple (iOS
	6).</p>

