	<p>De iPhone 4S is een smartphone ontworpen en op de markt gebracht door
	Apple Inc. Op 4 oktober 2011 werd de iPhone 4S door Apple
	geïntroduceerd. Deze werd vanaf 14 oktober verkocht in de Verenigde
	Staten, het Verenigd Koninkrijk, Frankrijk, Duitsland, Japan, Canada en
	Australië. Vanaf 28 oktober werd de iPhone 4S ook verkocht in
	verschillende andere landen waaronder Nederland en België. De iPhone 4S
	kreeg onder andere een Apple A5-chip, die ook in de iPad 2 zit en een 8
	megapixelcamera. Hij was net als de iPhone 4 in een zwarte en witte
	uitvoering verkrijgbaar en kreeg naast een 16GB- en 32GB-versie ook een
	versie met 64 GB geheugen. De iPhone 4S was de eerste iPhone die zowel
	het GSM- als het CDMA-netwerk ondersteunt. Sinds september 2013 is het
	toestel ook beschikbaar met 8 GB geheugen.</p>

	<h2>Functies</h2>

	<p>Deze versie van de iPhone, die op 4 oktober 2011 tijdens een speciale
	persconferentie werd aangekondigd, bevat betere hardware en een nieuwe
	spraakbesturing genaamd Siri. Het ontwerp van de iPhone 4S is nauwelijks
	veranderd ten opzichte van de iPhone 4. Er is enkel een kleine
	verandering in de antenneband om de problemen waarmee iPhone
	4-gebruikers kampten op te lossen. De iPhone 4S heeft twee antennes voor
	verbeterde ontvangst en verbeterde gesprekskwaliteit. De iPhone 4S heeft
	dezelfde Apple A5-chip als te vinden in de iPad 2. De A5-chip is een
	dual-coreprocessor, in tegenstelling tot de A4-chip die een
	single-coreprocessor is, met een kloksnelheid van 1 GHz voor elke kern,
	echter is deze geklokt op 800Mhz om batterij te besparen. Daarnaast is
	de A5-chip grafisch gezien ongeveer zeven keer zo krachtig als zijn
	voorganger. De iPhone 4S heeft een 8 megapixelcamera die in staat is om
	in HD-1080p30fps-kwaliteit te filmen met videostabilisatie. Daarnaast
	kan de camera tot tien gezichten tegelijkertijd herkennen en zich daarop
	focussen. Apple claimt daarnaast dat de camera van de iPhone 4S de
	snelste smartphonecamera op de markt is. Het duurt 1,1 seconde om de
	camera-applicatie te openen en een foto te nemen, en vervolgens 0,5
	seconden om nog een foto te nemen. De grootste vernieuwing is het
	vernieuwde spraakbesturingssysteem, Siri. Siri probeert context uit een
	zin te halen in plaats van naar bepaalde woorden te zoeken, zoals
	voorheen gebeurde. Siri maakt het mogelijk om met je stem een
	telefoongesprek te starten, een notitie aan te maken, je agenda bij te
	werken, sms-berichten te sturen, maar je kan er ook gewoon mee kletsen.
	Siri kan ook gesproken tekst omzetten in woorden en is beschikbaar in
	elke applicatie waarbij je normaal gesproken zou moeten typen. Siri is
	momenteel alleen beschikbaar in het Engels, Duits, Frans, Japans,
	Chinees, Koreaans, Italiaans en Spaans. Het is de verwachting dat Siri
	pas ergens in 2013 ook het Nederlands zal ondersteunen. De iPhone 4S
	ondersteunt ook de CDMA-technologie. Dit is een tegenhanger van de
	GSM-technologie die onder andere gebruikt wordt in de Verenigde Staten
	en Azië. Daardoor is het vanaf nu mogelijk om de iPhone 4S op vrijwel
	elk mobiel netwerk in de wereld te gebruiken. Apple heeft daarnaast nog
	kleinere hardwarevernieuwingen doorgevoerd in de iPhone 4S. De iPhone 4S
	ondersteunt nu bluetooth 4.0, de Russische variant van het Amerikaanse
	gps-systeem GLONASS, heeft een helderder scherm, een luidere speaker en
	een iets betere batterij. De iPhone 4S is voor het eerst ook in een
	64GB-variant beschikbaar. De iPhone 4S is verkrijgbaar in de kleuren wit
	en zwart, wordt geleverd met iOS 5 en is verkrijgbaar als 16GB-, 32GB-
	of 64GB-model.</p>

